    private void exibieinfo(){
        ArrayList<CadastroClass> cadastro=cvc.retornar();
     
        for(int i=0;i<500;i++){
           tabela.setValueAt("",i,0);
            tabela.setValueAt("",i,1);
             tabela.setValueAt("",i,2);
              tabela.setValueAt("",i,3);
               tabela.setValueAt("",i,4);
        }
        for(int s=0;s<500;s++){
            tabela.setValueAt(cadastro.get(s).getNome(),s,0);
            tabela.setValueAt(cadastro.get(s).getTipo(),s,1);
            tabela.setValueAt(cadastro.get(s).getSexo(),s,2);
            tabela.setValueAt(cadastro.get(s).getLevel(),s,3);
            tabela.setValueAt(cadastro.get(s).getDescricao(),s,4);
        }
    }
